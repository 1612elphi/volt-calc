// Post-build: turn Vite's self-contained dist/index.html into a drop-in
// fragment (no <!doctype>/<html>/<head>/<body>) shaped like OLD-CALC-SNIPPET,
// ready to paste into an existing voltsite page.
//
//   <div id="kita-zuschuss-rechner-root"></div>
//   <script type="module"> … inlined React bundle (CSS lives inside it) … </script>
//
// The widget renders into a Shadow DOM and injects its stylesheet THERE, so the
// fragment ships NO global <style> at all — the host page receives zero CSS from
// the widget and cannot be restyled by it (this is what previously broke
// voltsite's global sidebar: a second global Tailwind whose `.hidden`/utilities
// collided with the host's). build-droppable asserts that invariant below.
//
// INTEGRATION NOTE: emit this fragment as RAW HTML (a Kirby template/snippet or
// a raw-HTML field) — NOT through a kirbytext/Markdown textarea, which would
// smartypants the quotes/dashes in the bundle and corrupt it. It ships an inline
// <script type="module">; if the host ever adopts a strict CSP, allow it via a
// nonce/hash or serve the bundle as an external file.

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(root, "dist/index.html");
// Emitted at the project root (next to OLD-CALC-SNIPPET.htm) — this is the file
// to paste into voltsite.
const OUT = resolve(root, "kita-zuschuss-rechner.droppable.html");

const html = readFileSync(SRC, "utf8");

// The inlined application bundle (the CSS string is bundled inside it). Keep
// type="module"; drop crossorigin (inline). Re-escape any literal </script in
// the body so it can't terminate the tag early (esbuild already escapes it;
// belt-and-suspenders).
const scripts = [...html.matchAll(/<script\b([^>]*)>([\s\S]*?)<\/script>/gi)].map((m) => {
  const isModule = /type=["']module["']/i.test(m[1]);
  const body = m[2].replace(/<\/script/gi, "<\\/script");
  return `<script${isModule ? ' type="module"' : ""}>${body}</script>`;
});

const div = (html.match(/<div\s+id=["']kita-zuschuss-rechner-root["'][^>]*>\s*<\/div>/i) || [])[0];

if (scripts.length === 0) throw new Error("build-droppable: no <script> bundle found");
if (!div) throw new Error("build-droppable: mount root #kita-zuschuss-rechner-root missing");

// Host-safety invariant: every <style> in the build is dev-only chrome (the
// Ubuntu webfont + page background used by the standalone preview). The widget's
// own CSS must travel inside the bundle and be injected into the Shadow DOM, NOT
// shipped as a global <style>. If a non-dev <style> appears here, the widget
// would once again restyle the host — fail the build loudly.
const leakedStyles = [...html.matchAll(/<style\b([^>]*)>([\s\S]*?)<\/style>/gi)].filter(
  (m) => !/\bid=["']kzr-dev-only["']/i.test(m[1]) && m[2].trim() !== ""
);
if (leakedStyles.length > 0) {
  throw new Error(
    "build-droppable: a global <style> survived — the widget CSS must be injected into the Shadow DOM, not shipped globally"
  );
}

// The non-greedy bundle regex is only safe while there is exactly one real
// </script> in the document (esbuild escapes every other occurrence). Assert it,
// so a future build that emitted an unescaped </script> fails loudly instead of
// silently shipping a truncated bundle.
if (html.indexOf("</script>") !== html.lastIndexOf("</script>")) {
  throw new Error("build-droppable: more than one literal </script> in dist — bundle extraction would truncate");
}

const fragment = `<!-- Kita-Zuschuss Rechner — Volt Karlsruhe. Self-contained drop-in fragment.
     Generated by build-droppable.mjs — do not edit by hand. No runtime CDNs.
     Renders into a Shadow DOM; ships zero global CSS, so it never touches the host.
     Emit as raw HTML (template/snippet or raw field), not via kirbytext/Markdown. -->
${div}
${scripts.join("\n")}
`;

writeFileSync(OUT, fragment, "utf8");
console.log(`build-droppable: wrote ${OUT} (${fragment.length.toLocaleString("en-US")} bytes)`);
